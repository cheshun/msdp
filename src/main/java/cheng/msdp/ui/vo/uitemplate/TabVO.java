package cheng.msdp.ui.vo.uitemplate;

public class TabVO {
	
	private String tabname ;
	private String tabcode ;
	private String pk_tabtemplate ;
	public String getTabcode() {
		return tabcode;
	}
	public void setTabcode(String tabcode) {
		this.tabcode = tabcode;
	}
	public String getPk_tabtemplate() {
		return pk_tabtemplate;
	}
	public void setPk_tabtemplate(String pk_tabtemplate) {
		this.pk_tabtemplate = pk_tabtemplate;
	}
	public String getTabname() {
		return tabname;
	}
	public void setTabname(String tabname) {
		this.tabname = tabname;
	}
}
